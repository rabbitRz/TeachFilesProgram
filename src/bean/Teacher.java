package bean;

import java.sql.Date;

public class Teacher {
	int id;//编号
	String name;//姓名
	String sex;//性别
	String nativeplace;//籍贯
	Date birthday;//出生日期
	String education;//学历
	String title;//职称
	Date quatime;//取得资格时间
	String graschool;//毕业学校
	String major;//专业
	String workingtime;//参加工作时间
	String currentmajor;//现从事专业
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getNativeplace() {
		return nativeplace;
	}
	public void setNativeplace(String nativeplace) {
		this.nativeplace = nativeplace;
	}
	public Date getBirthday() {
		return birthday;
	}
	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}
	public String getEducation() {
		return education;
	}
	public void setEducation(String education) {
		this.education = education;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Date getQuatime() {
		return quatime;
	}
	public void setQuatime(Date quatime) {
		this.quatime = quatime;
	}
	public String getGraschool() {
		return graschool;
	}
	public void setGraschool(String graschool) {
		this.graschool = graschool;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getWorkingtime() {
		return workingtime;
	}
	public void setWorkingtime(String workingtime) {
		this.workingtime = workingtime;
	}
	public String getCurrentmajor() {
		return currentmajor;
	}
	public void setCurrentmajor(String currentmajor) {
		this.currentmajor = currentmajor;
	}
	
	
	
	

}
